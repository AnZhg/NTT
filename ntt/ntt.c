    (*io)[ 5] = (tmp[ 5] +  81 * tmp[ 7]) % 193;
    (*io)[ 6] = (tmp[ 4] + 192 * tmp[ 6]) % 193;
    (*io)[ 7] = (tmp[ 5] + 112 * tmp[ 7]) % 193;
    (*io)[ 8] = (tmp[ 8] +       tmp[10]) % 193;
    (*io)[ 9] = (tmp[ 9] +  81 * tmp[11]) % 193;
    (*io)[10] = (tmp[ 8] + 192 * tmp[10]) % 193;
    (*io)[11] = (tmp[ 9] + 112 * tmp[11]) % 193;
    (*io)[12] = (tmp[12] +       tmp[14]) % 193;
    (*io)[13] = (tmp[13] +  81 * tmp[15]) % 193;
    (*io)[14] = (tmp[12] + 192 * tmp[14]) % 193;
    (*io)[15] = (tmp[13] + 112 * tmp[15]) % 193;
    
    // 8-Point.
    tmp[ 0] = ((*io)[ 0] +       (*io)[ 4]) % 193;
    tmp[ 1] = ((*io)[ 1] +   9 * (*io)[ 5]) % 193;
    tmp[ 2] = ((*io)[ 2] +  81 * (*io)[ 6]) % 193;
