    tmp[ 9] = ((*io)[ 9] +   9 * (*io)[13]) % 193;
    tmp[10] = ((*io)[10] +  81 * (*io)[14]) % 193;
    tmp[11] = ((*io)[11] + 150 * (*io)[15]) % 193;
    tmp[12] = ((*io)[ 8] + 192 * (*io)[12]) % 193;
    tmp[13] = ((*io)[ 9] + 184 * (*io)[13]) % 193;
    tmp[14] = ((*io)[10] + 112 * (*io)[14]) % 193;
