    // 16-Point.
    (*io)[ 0] = (tmp[0] +       tmp[ 8]) % 193;
    (*io)[ 1] = (tmp[1] +   3 * tmp[ 9]) % 193;
    (*io)[ 2] = (tmp[2] +   9 * tmp[10]) % 193;
